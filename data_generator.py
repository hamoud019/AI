def gen_salutation():
    u = pick(["السلام عليكم","شماسي محمد؟","أهلاً يا محمد","اياك بخير ؟","اياك لباس؟","أهلاً وسهلاً","كيف الصحة؟","مرحبا"])
    f = pick(["وعليكم السلام ورحمة الله.","الحمد لله، أهلاً بيك.","وعليكم السلام، مرحبة بيك.","أهلاً وسهلاً، يا مرحبا."])
    m = pick([
        f"أنا بخير والحمد لله، {pick(أفعال_رعي)} من الصباح.",
        f"الحمد لله، {pick(ANIMAUX)} كلها بصحة.",
        f"بخير إن شاء الله، المرعى {pick(أوصاف_المرعى)} هذا الموسم.",
        f"تعبان شوي من {pick(أفعال_رعي)}، بس الحمد لله.",
    ])
    e = pick(["وأنت، كيف حالك؟","إن شاء الله أنت بخير.","الله يسلمك.","أنت منين؟","",""])
    return {"input": u, "output": join_sentences(f, m, e), "category": "salutation"}

